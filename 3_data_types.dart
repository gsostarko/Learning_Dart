void main() {
  //Data Types
  
  //Numbers, Strings, Boleans, Lists, Maps, Dynamic
  
  //Strings
  String firstName = "John";
  print("String: $firstName");
  
  //Integers
  int myNum = 41;
  print("Integer: $myNum");
  
  //Doubles
  double otherNum = 3.14;
  print("Double: $otherNum");
  
  //Booleans
  bool myBool = false;
  print("Boolean: $myBool");
  
  //Dynamic
  dynamic fullName = "Goran Sostarko";
  print("Dynamic: $fullName");
  
}